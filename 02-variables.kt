fun main() {

    var myVariable: Int = 5
    println("Initial value: $myVariable")

    println()//print a blank line

    val name: String = "Kotlin"      // Immutable variable
    var age: Int = 10                // Mutable variable

    println("Hello, $name! You are $age years old.")
    age += 1
    println("Next year, you will be $age years old.")
    
}